package ca.group8.gameservice.splendorgame.model.splendormodel;

/**
 * Available Powers in trading post extension.
 */
public enum PowerEffect {
  /**
   * Take 2 tokens of the same colour get another.
   */
  TWO_PLUS_ONE,
  /**
   * Gold tokens have double value.
   */
  DOUBLE_GOLD,
  /**
   * Free token everytime you purchase a development card is purchased.
   */
  EXTRA_TOKEN,
  /**
   * Get 5 prestige points.
   */
  FIVE_POINTS,
  /**
   * Get a prestige point for every trading route unlocked.
   */
  ARM_POINTS
}
